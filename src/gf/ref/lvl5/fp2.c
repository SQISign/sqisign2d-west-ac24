uint32_t fp2_decode(fp2_t *d, const void *src){
    const uint8_t *buf = src;
    uint32_t re, im;
    
    re = fp_decode(&(d->re), buf);
    im = fp_decode(&(d->im), buf + FP_ENCODED_BYTES);
    return re & im;
}

